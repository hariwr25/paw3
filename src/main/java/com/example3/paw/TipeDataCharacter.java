package com.example3.paw;

public class TipeDataCharacter {
    public static void main(String[] args) {
        char character1 = 'A';
        char character2 = 'B';
        char character3 = '5';
        char character4 = '!';

        System.out.println("Karakter 1: " + character1); // Output: Karakter 1: A
        System.out.println("Karakter 2: " + character2); // Output: Karakter 2: B
        System.out.println("Karakter 3: " + character3); // Output: Karakter 3: 5
        System.out.println("Karakter 4: " + character4); // Output: Karakter 4: !
    }
}