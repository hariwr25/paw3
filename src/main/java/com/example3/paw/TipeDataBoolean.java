package com.example3.paw;

public class TipeDataBoolean {
      public static void main(String[] args) {
        boolean isTrue = true;
        boolean isFalse = false;

        System.out.println("Nilai isTrue: " + isTrue);   // Output: Nilai isTrue: true
        System.out.println("Nilai isFalse: " + isFalse); // Output: Nilai isFalse: false

        // Contoh penggunaan tipe data boolean dalam suatu kondisi
        int x = 5;
        int y = 10;
        boolean isXGreaterThanY = x > y;
        System.out.println("Apakah x lebih besar dari y? " + isXGreaterThanY); // Output: Apakah x lebih besar dari y? false
    }
}